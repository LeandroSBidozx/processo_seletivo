#definição das variáveis.
indice = 13
soma = 0
k = 0

#enquanto k for menor que "indice"
while k < indice:
    #incrementa ao k, o valor de 1
    k += 1
    #incrementa á soma, o valor de k
    soma += k
#mostra na tela o valor de "soma"
print(soma)
# O resultado ao final do processamento é 91.
